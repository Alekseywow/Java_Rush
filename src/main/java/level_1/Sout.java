package level_1;

public class Sout {

    public static void main(String[] args) {

        // System.out.println(); - Выводит текст на несколько строк
        System.out.println("Пример Мир!");
        System.out.println("Робот друг человека!");
        System.out.println("Пристанище живых!!");

        System.out.println("------");

        // System.out.print(); - Выводит текст в одну строку
        System.out.print("Amogo");
        System.out.print("The");
        System.out.println("Best");

        System.out.println("------");

        // Составление фразы по частям и вывод в две строки
        System.out.print("Я");
        System.out.print(" люблю");
        System.out.println(" Java!");
        System.out.print("Это");
        System.out.println(" интересно!");

    }
}
